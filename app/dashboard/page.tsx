const ALL_DL_SPECIES: SpeciesKey[] = ["elk", "mule_deer", "whitetail", "pronghorn", "bighorn_sheep", "mountain_goat", "moose", "black_bear", "bison"];
const DL_MONTH_NAMES = ["Jan","Feb","Mar","Apr","May","Jun","Jul","Aug","Sep","Oct","Nov","Dec"];
const POINT_SYSTEM_LABELS: Record<string, string> = { preference:"Preference pts", bonus:"Bonus pts", weighted:"Weighted pts", none:"Random lottery", otc:"OTC" };

interface DeadlineItem {
  stateId: string; stateName: string; species: SpeciesKey;
  closeMonth: number; closeDay: number; closeYear: number;
  openMonth: number; feeNonresident: number; hasOTC: boolean;
  pointSystem: string; daysUntil: number; isOpenNow: boolean;
}

function getDeadlines(speciesFilter: SpeciesKey | "all"): DeadlineItem[] {
  const now = new Date(); const today = now.getTime(); const currentMonth = now.getMonth() + 1;
  const seen = new Set<string>(); const deadlines: DeadlineItem[] = [];
  for (const d of huntingData) {
    if (speciesFilter !== "all" && d.species !== speciesFilter) continue;
    const key = `${d.stateId}-${d.species}`; if (seen.has(key)) continue; seen.add(key);
    let year = now.getFullYear();
    let closeDate = new Date(year, d.appCloseMonth - 1, d.appCloseDay);
    if (closeDate.getTime() < today) { year += 1; closeDate = new Date(year, d.appCloseMonth - 1, d.appCloseDay); }
    const daysUntil = Math.ceil((closeDate.getTime() - today) / 86400000);
    let isOpenNow = false;
    if (year === now.getFullYear()) {
      if (d.appOpenMonth <= d.appCloseMonth) isOpenNow = currentMonth >= d.appOpenMonth && currentMonth <= d.appCloseMonth && closeDate.getTime() > today;
    } else { isOpenNow = currentMonth >= d.appOpenMonth; }
    deadlines.push({ stateId: d.stateId, stateName: d.stateName, species: d.species as SpeciesKey, closeMonth: d.appCloseMonth, closeDay: d.appCloseDay, closeYear: year, openMonth: d.appOpenMonth, feeNonresident: d.feeNonresident, hasOTC: d.hasOTC, pointSystem: d.pointSystem, daysUntil, isOpenNow });
  }
  return deadlines.sort((a, b) => a.daysUntil - b.daysUntil);
}

