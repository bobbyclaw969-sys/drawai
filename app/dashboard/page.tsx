function getDeadlineForState(stateId: string, species: string): { closeMonth: number; closeDay: number } | null {
  const entry = huntingData.find(d => d.stateId === stateId && d.species === species);
  if (!entry) return null;
  return { closeMonth: entry.appCloseMonth, closeDay: entry.appCloseDay };
}

function buildPrefillCard(profile: HunterProfile, guide: StateApplyGuide, species: string): string {
  const speciesLabel = SPECIES_LABELS[species as SpeciesKey] ?? species;
  const deadline = getDeadlineForState(guide.stateId, species);
  const deadlineStr = deadline
    ? `${DRAW_RESULT_MONTHS[deadline.closeMonth]} ${deadline.closeDay}`
    : "Check state agency";
  const lines: string[] = [
    `╔══════════════════════════════════════════╗`,
    `  ${guide.stateName.toUpperCase()} — ${speciesLabel.toUpperCase()} APPLICATION`,
    `  Tag Hunter Pre-Fill Card`,
    `╚══════════════════════════════════════════╝`,
    ``,
    `PERSONAL INFO`,
    `Name:      ${profile.firstName} ${profile.lastName}`,
    `DOB:       ${profile.dob || "—"}`,
    `Address:   ${profile.address}`,
    `           ${profile.city}, ${profile.residencyState} ${profile.zip}`,
    `Phone:     ${profile.phone || "—"}`,
    `Email:     ${profile.email || "—"}`,
    `SSN Last4: ${profile.ssn4 || "—"}`,
    ``,
    `HUNTER EDUCATION`,
    `Cert #:    ${profile.hunterEdNumber || "—"}`,
    `Issued by: ${profile.hunterEdState || "—"}`,
    ``,
  ];
  const licenseNum = profile.licenses?.[guide.stateId];
  if (licenseNum) {
    lines.push(`${guide.stateName.toUpperCase()} LICENSE`);
    lines.push(`License #: ${licenseNum}`);
    lines.push(``);
  }
  lines.push(`APPLICATION DETAILS`);
  lines.push(`State:     ${guide.stateName}`);
  lines.push(`Species:   ${speciesLabel}`);
  lines.push(`Deadline:  ${deadlineStr}`);
  lines.push(`Portal:    ${guide.portalUrl}`);
  lines.push(``);
  if (guide.specialRequirements.length > 0) {
    lines.push(`BEFORE YOU APPLY`);
    guide.specialRequirements.forEach(r => lines.push(`• ${r}`));
    lines.push(``);
  }
  lines.push(`─────────────────────────────────────────`);
  lines.push(`Generated by Tag Hunter · taghunter.app`);
  lines.push(`Always verify fees & deadlines at official state agency.`);
  return lines.join("\n");
}

function DeadlinePill({ stateId, species }: { stateId: string; species: string }) {
  const dl = getDeadlineForState(stateId, species);
  if (!dl) return null;
  const now = new Date();
  let year = now.getFullYear();
  let closeDate = new Date(year, dl.closeMonth - 1, dl.closeDay);
  if (closeDate < now) { year++; closeDate = new Date(year, dl.closeMonth - 1, dl.closeDay); }
  const days = Math.ceil((closeDate.getTime() - now.getTime()) / 86400000);
  return (
    <span
      style={{
        fontFamily: MONO,
        fontSize: 10,
        fontWeight: 500,
        letterSpacing: "0.1em",
        textTransform: "uppercase",
        padding: "2px 8px",
        border: `1px solid ${FENCE}`,
        color: DUST,
        whiteSpace: "nowrap",
      }}
    >
      {APPLY_MONTHS[dl.closeMonth]} {dl.closeDay} · {days}d
    </span>
  );
}

